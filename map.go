// -- map

// a multi-layered map
type Map struct {
	bottom [][]int
	middle [][]int
	top    [][]int
}

func (m *Map) NumLevels() int {
	return 4
}

func (m *Map) Level(level int) [][]int {
	if level == 0 {
		return m.bottom
	} else if level == 1 {
		return m.middle
	} else {
		return m.top // if above highest level just keep extending last one up
	}
}

